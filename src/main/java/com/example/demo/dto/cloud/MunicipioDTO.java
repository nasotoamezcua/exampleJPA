package com.example.demo.dto.cloud;

public class MunicipioDTO {
	
	private Integer id;
	private String nombre;
	private Integer idRenapo;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Integer getIdRenapo() {
		return idRenapo;
	}
	public void setIdRenapo(Integer idRenapo) {
		this.idRenapo = idRenapo;
	}

}
